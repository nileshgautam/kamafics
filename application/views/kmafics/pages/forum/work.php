                                    <div class="wt-post-text">
                                        <h2 class="font-weight-bold">What is Make in India:</h2>
                                        <p>
                                            Make in India is an initiative launched by Hon’ble Prime Minister of India, Shri Narendra Modi, when he came to power in 2014 to an economy that was witnessing its lowest growth rate in decades.</p>

                                        <p>The economic bubble had just burst, and dubious eyes were looking at India’s future and wondering if this giant economy would live up to its aspirations.</p>

                                        <p>At this point, the Make in India initiative was a breath of fresh air, breathing life into India’s economy and, more importantly, its government mindset.</p>

                                        <p>The focus had changed from being a facilitator of businesses to being a business partner of the businesses. This was a crucial shift in mindset as it meant that the Government was no longer a spectator but a player in the economy hand in hand with the very businesses that sustain the economy.
                                        </p>

                                        <p>At this point, a new question was raised, which was ,”Is make in India necessary?”

                                            The answer was a resounding Yes!
                                        </p>
                                        <p>India faced many problems at the time which were:
                                            <ol class="ml-5">
                                                <li>A manufacturing sector that was not able to keep up with the other sectors</li>
                                                <li>A growth rate of the economy which had become the lowest it had been in years</li>
                                                <li>Lack of job opportunities in India</li>
                                                <li> Lack of funds available to kickstart the growth of the manufacturing sector and to put it on par with the other developed countries benchmarks.</li>
                                                <li>The service sector was responsible for a 57% share in the Indian Economy. Conversely, it was generating only 27% of job opportunities for the Indian labor force, with the remainder not being shouldered by the other sectors effectively.
                                                </li>
                                            </ol>
                                        </p>
                                        <p>These are but a few of the problems that India faced at the time that necessitated the advent of Make in India, and it was introduced keeping these overarching objectives in mind.</p>

                                        <h3 class="font-weight-bold">Objectives of Make in India:</h3>
                                        <ol class="ml-5">
                                            <li>To revive the manufacturing sector and increase the growth rate of the economy.
                                            </li>
                                            <li>To encourage foreign direct investment into the Indian Economy.</li>
                                            <li>To increase the “Ease of Doing Business” so that more companies and businesses manufacture within India.</li>
                                            <li>To develop India into a global manufacturing hub and increase the employment opportunities available in India.</li>
                                            <li>To open up and develop 25 sectors such as autocomponents, biotechnology and defence manufacturing to name a few.</li>
                                        </ol>
                                        <div class="wt-post-media wt-img-effect">
                                            <a href="javascript:;"><img src="<?php echo base_url('assets/images/blog/default/i2.jpg') ?>" alt=""></a>
                                        </div>
                                        <h3 class="font-weight-bold py-2">How did Make in India achieve its objective?</h3>
                                        <ol class="ml-5">
                                            <li>By reducing the forms and regulations required or consolidating them into single forms and introducing new web portals that simplify the process of filing the forms.</li>
                                            <li>By reducing the amount of inspection and formalities required in the beginning stages of opening a business.</li>
                                            <li>By amending the existing acts such as the Companies Act to increase the ease of doing business.</li>
                                            <li>By incorporating the leading advancements in online administration such as digital signatures and the like.</li>
                                            <li>By reducing the time it takes to complete applications and receive permits.</li>
                                            <li>By reducing the costs involved in obtaining permits.</li>
                                            <li>Reduction of Mandatory documents required for import/export.</li>
                                            <li>Introduction of computerized risk management systems which have improved transparency and lowered the count of the required customs inspections.</li>
                                            <li>The commercial courts and appellate bodies of the courts have been established in Mumbai and Delhi to provide a fast track solution to the legal issues that businesses face.</li>
                                            <li>Amendments to the credit-related acts to incorporate a greater ambit of the properties involved.</li>
                                            <li>Increase the ease of obtaining electricity and capping the time limits as well as the costs involved.</li>
                                            <li> Digitization of land records has increased transparency and search ability.</li>
                                            <li>Reduction of tax rates for companies and the creation of other concessional tax regimes that can be availed.</li>
                                            <li>Introduction of the Goods and Services Tax (G.S.T) that consolidated the different taxes into a more efficient tax system.</li>
                                            <li>Reduction of physical touchpoints for the processes of filing forms and applications.</li>





                                        </ol>
                                        <h3 class="font-weight-bold"> Was Make in India successful?</h3>

                                        <p> There is no doubt that Make in India has been wildly successful as India’s position in the World Bank Ease of Doing Business list has improved from 142 in 2014 to the 63rd rank in 2019.</p>

                                        <p> This is a massive jump by any measure, and it is even more impressive when you consider that it is the third year in a row that India has reached the “Top 10 Improvers” in the Ease of Doing Business list.</p>

                                        <p> Of course, India still faces many challenges in improving its rank further, but the winds of change seem to be blowing positively for India. More companies and businesses will take advantage of this fair wind to set up their manufacturing process in India, thus achieving India’s objectives of Make in India.</p>
