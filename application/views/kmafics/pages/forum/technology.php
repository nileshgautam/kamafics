                                        <h3 class="font-weight-bold">Is Augmented Reality limited?</h3>
                                        <p>The answer is NO. It is only limited to our imaginations, as has been portrayed by several companies incorporating it within their operations.</p>
                                        <p>We’ve mentioned the example of Pokemon GO and apps such as Instagram and Snapchat, however, that is just the tip of the iceberg for Augmented Reality’s applications.</p>
                                        <p>It can be implemented for educational training, interactive classrooms and offices, immersive travel experiences, viewing how furniture looks within homes without actually buying it (IKEA), shopping for clothes (UNIQLO), etc.</p>
                                        <p>All this reinforces our point that AR is limited only by our imagination and the constraints of technology that existed are slowly being freed as the technology advances.</p>

                                        <h3 class="font-weight-bold">Is Augmented Reality available to all?</h3>
                                        <p>It is to a certain extent. More and more smartphones, a device that most of us use, have augmented reality features installed onto it.</p>
                                        <p>Furthermore, due to research and development, the market is not limited to smartphones and is leading to further inventions which utilizes augmented reality as their base—such as the HoloLens from Microsoft, Google Smart Glasses et cetera.</p>
                                        <h3 class="font-weight-bold">How is Augmented Reality Useful for Businesses?</h3>
                                        <p>Businesses can immerse the customer into a simulation, which shows them exactly what would happen should they purchase the products on sale. This is an irresistible lure for the consumer since he/she can verify whether they want the product, and once their mind is made up, it’s effortless to make the sale.</p>
                                        <p>Businesses can also use Augmented Reality to spread their company values and other messages to the customers. The ease of doing so has dramatically increased with the proliferation of augmented reality capable devices such as smartphones, smartwatches, et cetera.</p>
                                        <p>The market is portrays Augmented Reality as the next big thing with the market valuation at $ 4.3 billion in 2019 and projected at an 86 per cent growth to $ 8 billion in 2020.
                                        </p>
