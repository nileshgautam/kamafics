                                        <h2 class="post-title font-weight-bold">Augmented Reality - Future or Fad?</h2>
                                    </div>

                                    <div class="wt-post-text">
                                        <b>Virtual Reality is the future. </b>
                                        <p>It is something which is widely accepted by the masses, however, the underlying reality remains that the future seems far away. Despite repeated advancements in technology, virtual reality has just barely crossed the functional threshold in terms of practicality, usage and utility.</p>

                                        <p>Augmented Reality, on the other hand, is the present. It already exists, and it’s being incorporated within various fields with great success. The best example of this is the game Pokemon Go. It was a worldwide phenomenon that brought people out of their houses and relive their childhood memories and the root of it was its augmented reality function.</p>

                                        <p>Looking at the future of Augmented Reality, technological giants such as Google, Microsoft, Apple, Facebook, Samsung et cetera have deployed a major chunk of their resources for research, development and implementation of AR, hence, banking on its success.</p>

                                        <p>But for the average individual, Augmented Reality seems to be more of a fad than the future. It appears to be seasonal in nature with a constant trend of peak and trough.
                                        </p>
                                        <p>This is accentuated by the usage of augmented reality for the average individual. They are limited to using filters on social media applications such as Instagram, Snapchat et cetera and envision to transform their environment according to their preferences.</p>



                                        <blockquote>
                                            <i class="fa fa-quote-left font-20"></i>
                                            <p>But is this true?.</p>

                                        </blockquote>
