                                    <div class="wt-post-title ">
                                        <h2 class="post-title">Workplaces after Covid-19</h2>
                                    </div>

                                    <div class="wt-post-text">
                                        <p> As the pandemic struck, businesses scrambled to reinvent themselves and their operations in a bid to adapt to the ever-changing turbulent times that followed.</p>
                                        <p>For some businesses and companies, this meant that their entire work-force was shifted to work from home status. For others, it meant a period of reinventing and reutilizing the available spaces in an attempt to mitigate the spread of the coronavirus and resume normal operations as soon as possible.</p>
                                        <p>Today, we’re into the pandemic by a couple of months now, and there is hope for a vaccine over the horizon soon.</p>

                                        <p>This, unfortunately, also means that some businesses are unwilling to step forward and implement the best practices that are effective in dealing with coronavirus, and for that matter, any pandemic.
                                        </p>
                                        <p>This is because it requires significant investment, which seems redundant, especially with the unstable cashflows.</p>
                                        <p>But who is to say if the future holds another pandemic like the coronavirus and if it does, only the businesses who adapted will survive.</p>
                                        <p>
                                            By adaptation, I mean the businesses who have adapted practices such as:
                                        </p>
                                        <ol class="ml-5">

                                            <li>
                                                <h3 class="font-weight-bold">Focus on Hygiene:</h3>
                                                <p>By ensuring that the common areas are earmarked and sanitized regularly. Spreading awareness and mandating regular sanitization of hands all across the office landscape. This has significantly mitigated the risk of spread and infection of the virus. It requires remapping the office to incorporate these new elements.</p>
                                            </li>
                                            <li class="py-2">
                                                <h3 class="font-weight-bold">Focus on the Health of the Employees:</h3>
                                                <p>Previously, we had a culture where if you were mildly sick, you could still be called to show up at work. Today this has reversed, and instead, the employees’ health is monitored religiously, and the slightest sign of sickness would lead to the employee being given work from home status.</p>
                                            </li>
                                            <li class="py-2">
                                                <h3 class="font-weight-bold">Focus on Air Circulation:</h3>
                                                <p>
                                                    As per the studies, the virus tends to survive on surfaces and areas where air circulation is not very effective such as in air-conditioned environments. This is why there is a need for effective and efficient air circulation systems that can also be cleaned regularly and disinfected. As of now, the majority of the existing systems are within the walls and ducts of the offices and inaccessible except for the annual or bi-annual deep cleaning. However, currently, it is being dealt with by switching off the air conditioning units and installing air purifiers at strategic positions.
                                                </p>
                                            </li>
                                            <li class="py-2">
                                                <h3 class="font-weight-bold">Focus on Limited Employees Within the Office:</h3>
                                                <p>Instead of having all the existing employees within the office, with the current scenario, it is beneficial and safer to have only a small number of the essential employees working in the office at any given time. This has been achieved through-</p>
                                                <ol class="ml-5 ">
                                                    <li>
                                                        <strong>Focusing on Rotating Days:</strong>
                                                        <p>We are all familiar with the idea of shifts. But now, instead of shifts being utilized to fill up the entire day, it will be used to fill up every alternate day for separate groups of employees. That is the concept of rotating days and has been implemented successfully in many companies already. This measure is to be complemented by:</p>
                                                    </li>
                                                    <li>
                                                        <strong>Focusing on Decentralization:

                                                        </strong>
                                                        <p>The existing traditional practice is to have a large headquarters that can hold almost all the employees of the company within it. However, the novel coronavirus has led to this being a hazardous situation. Thus an alternative had to be found, and that alternative is-
                                                            <ol class="ml-5">
                                                                <li><strong>Focusing on Distributing Offices:


                                                                    </strong>
                                                                    <p>By distribution of offices, we mean that the offices are going to be created and divided in such a way that the number of employees within each office is a “safe” number. This can lead to the offices being spread far and wide and gives more autonomy to the local managers.
                                                                        This is also helpful in case an employee is infected mitigatory measures for quarantine can be implemented immediately with minimal loss in work efficiency.</p>
                                                                </li>
                                                            </ol>
                                                        </p>
                                                    </li>
                                                </ol>
                                            </li>
                                            <li class="py-2">
                                                <h3 class="font-weight-bold">Focus on more Utilitarian Designs:</h3>
                                                <p>The requirements of furniture and office space utilization will tend to lean towards those materials that can undergo continuous and heavy sterilization using caustic substances. The office space will also have to be redesigned in such a way that the employees are separated, and the concept of open working spaces might just be eliminated. </p>
                                            </li>
                                            <li>
                                                <h3 class="font-weight-bold">Focus on Technology:</h3>
                                                <p>Technology such as video conferencing, office social solutions like slack, cloud-based work, and meetings, data management systems, etc. Many companies have switched entirely to virtual operational practices that rely heavily on such applications and software. </p>
                                            </li>
                                            <li>

                                                <h3 class="font-weight-bold">Focus on Contactless offices:</h3>
                                                <p>Replacing automation for all manual tasks such as operating the lift, self-service at the pantry, getting a cup of coffee, etc.</p>

                                                <p>This can be through apps that govern these machines on your phone, which you can use as you would your card, and so on.</p>

                                                <p>One of the first buildings in the world to install these features is the Gateway Building in Australia which has palm and fingers scanning at car parks, security gates, lifts, and lockers.

                                                </p>
                                                <div class="wt-post-media wt-img-effect">
                                                    <a href="javascript:;"><img src="<?php echo base_url('assets/images/blog/default/c2.jpg') ?>" alt=""></a>
                                                </div>

                                            </li>
                                            <li class="py-2">
                                                <h3 class="font-weight-bold">Focus on the Best Mental Health/Social Interaction/Cultural Practices for the future:</h3>
                                                <p>The shift in corporate culture and the work from home status of many employees has been a violent and turbulent change. This has placed more significant stress on the individuals who have to deal with the intermingling of their work and personal life, among many other factors, causes stress that needs to be handled properly.</p>

                                                <p>Social interaction has also taken a big hit, and even after the pandemic is over, workplace social interaction will never entirely be the same as it was before the pandemic and solutions for the same need to be found.</p>
                                                <p>

                                                    Without social interaction, the work from home employees will also face a significant disconnect between themselves, the work that they do, and the company itself. This problem existed before the coronavirus pandemic, but it has been accentuated in the wake of it, and the solution needs to be found.
                                                </p>
                                            </li>
                                            <li>
                                                <h3 class="font-weight-bold">Focus on Reskilling:</h3>
                                                <p>As the mad scramble in the wake of COVID-19 continues, it has become more imperative for all employees in the organization to learn other skills and especially skills relating to computer literacy and application.</p>

                                                <p>This has meant that intensive courses and webinars are provided to the employees to familiarize themselves with the new skills and thus reskill them so that they can reorient themselves in these changing times.
                                                </p>
                                            </li>
                                            <li>
                                                <h3 class="font-weight-bold">Focuson changing operational practices:</h3>
                                                <p>This pandemic has shown us that different operational practices such as work from home can be used successfully to overcome the limitations of a physical workplace but this comes with its own set of challenges.</p>

                                                <p>Challenges such as ensuring optimum productivity and employee wellbeing whilst at the same time considering the companies objectives.</p>

                                                <p>This has necessitated the implementation of new operational procedures such as mandatory screening at entrances, utilization of sanitizers, minimization of contact through handshakes, additional utilization of technology where possible to bypass human physical action, and so on.
                                                </p>
                                            </li>
                                            <li>
                                                <h3 class="font-weight-bold">Focusing on Trust:</h3>
                                                <p>People are now working with lesser supervision in an environment that is both foreign and familiar to them - their homes.</p>
                                                <p>
                                                    Thus, management is unable to oversee their work and their efficiency and have to rely on trusting them. Conversely, these employees have also started attending more meetings, albeit virtually, than ever before.</p>

                                                <p>This has led to deeper connections between individuals who are slowly adjusting to the new lifestyle of remote work.
                                                </p>
                                                <div class="wt-post-media wt-img-effect">
                                                    <a href="javascript:;"><img src="<?php echo base_url('assets/images/blog/default/c3.jpg') ?>" alt=""></a>
                                                </div>

                                            </li>
                                        </ol>





                                        <p>The novel coronavirus pandemic has seen businesses undergo a significant shift in the way that they conduct their operations. This has been touted to be both negative and positive.</p>

                                        <p>Negative because problems such as lack of social interaction, limited integration of culture, mental health issues of employees, health issues of the employees, etc. have received greater emphasis than ever before, but the relevant solutions have yet to be found.</p>
                                        <p>
                                            Positive because it has led to a reduction in expenses for companies as they downsize their office spaces and look for new solutions such as co-working spaces and the like to continue their operations. Along with this, the extra time that the employees can spend with their families is also an additional bonus.
                                        </p>
                                        <p>
                                            But the question is whether there are going to be more changes and upheavals occurring as a result of those changes in the future. That question, however, is one for the fates to decide.</p>
                                        <blockquote>What steps have you taken?</blockquote>



