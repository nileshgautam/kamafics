                                                            <li><strong>Speculation:</strong>A situation where people are investing and relying on their intuition as to whether there will be a profit or not. In most cases, this is serving to drive the stock prices higher and higher with no justification for the share price being at the level it is other than speculation. This is compounded by:</li>
